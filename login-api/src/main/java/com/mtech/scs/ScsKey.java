package com.mtech.scs;

public class ScsKey {

	String key;
	String id;
	
	public ScsKey(String key, String id) {
		this.key = key;
		this.id = id;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}
